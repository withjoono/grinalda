
export const getPagePath = pathName => {
    if(pathName == 'scoreInput') {
        return '/regular/scoreInput';
    } else if(pathName == 'analyse') {
        return '/regular/analyse';
    } else if(pathName == 'firstConsulting') {
        return '/regular/firstConsulting';
    } else if(pathName == 'secondConsulting') {
        return '/regular/secondConsulting';
    } else if(pathName == 'thirdConsulting') {
        return '/regular/thirdConsulting';
    }
}


